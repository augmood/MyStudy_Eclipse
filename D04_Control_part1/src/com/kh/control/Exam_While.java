package com.kh.control;

public class Exam_While {
	public static void main(String [] args) {
		int i = 9; // 초기식
		while(i>=0) { // 조건식
			System.out.println("출력된 i의 값 : "+i);
			i--; // 증가식
		}
		
		int j = 0; // 초기식
		while(j<10) { // 조건식
			System.out.println("출력된 i의 값 : "+j);
			j++; // 증가식
		}
	}

}
