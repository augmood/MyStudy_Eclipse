package com.kh.variable;

public class TestVarExam {
	public static void main(String [] args) {
		long lNum = 9999l;
		char firstChar = 'A';
		String firstStr = "Hello world";
		boolean result = true;
		float fNum = 486.520f;
		double dNum = 578.890123;
		
		System.out.println("정수의 값 : "+lNum);
		System.out.println("문자의 값 : "+firstChar);
		System.out.println("문자열의 값 : "+firstStr);
		System.out.println("논리의 값 : "+result);
		System.out.println("실수의 값 : "+fNum);
		System.out.println("실수의 값 : "+dNum);
	}

}
